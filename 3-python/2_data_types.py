# PRIMITIVE TYPES
a = 28          # int
b = 3.14        # float
i = True        # bool
m = None        # NoneType

# REFERENCE TYPES
d = "Hello"     # str
e = [1, 2, 3]   # list - mutable
f = (1, 2, 3)   # tuple - immutable
g = {1, 2, 3}   # set - unique, mutable
h = {"a": 1,"b": 2,"c": 3} # mutable key value pairs